package Calculator.Functions;

public class Numbers_Symbols {
    
    public double gete(){
        return Math.exp(1);
    }
    public double getPi(){
        return Math.PI;
    }

    public String openParenthesis(){
        return "(";
    }

    public String closeParenthesis(){
        return ")";
    }

    public String getComma(){
        return ".";
    }
}
